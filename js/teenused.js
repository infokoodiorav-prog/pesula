function muudaKeelTeenused(keel) {
  document.querySelector('.nupud a[href="index.html"]').textContent = teenusedTekstid[keel].nuppAvalehele;
  document.querySelector('.nupud a[href="broneering.html"]').textContent = teenusedTekstid[keel].nuppBroneeri;

  const teenusedElem = document.querySelectorAll('.teenused > div');
  const teenused = teenusedTekstid[keel].teenused;

  teenusedElem.forEach((div, index) => {
    if (teenused[index]) {
      div.querySelector('.esimene').textContent = teenused[index].nimi;
      div.querySelector('.teine').innerHTML = `${teenused[index].kirjeldus} <br>⏱️ Kestus: ${teenused[index].kestus}`;
    }
  });

  document.getElementById('oigus').textContent = teenusedTekstid[keel].oigus;
  document.getElementById('teine').textContent = teenusedTekstid[keel].teine;
}

const salvestatudKeel = localStorage.getItem('valitudKeel') || 'est';
muudaKeelTeenused(salvestatudKeel);

const keeleValik = document.getElementById('keeleValik');
if (keeleValik) {
  keeleValik.value = salvestatudKeel;
  keeleValik.addEventListener('change', () => {
    const valitud = keeleValik.value;
    localStorage.setItem('valitudKeel', valitud);
    muudaKeelTeenused(valitud);
  });
}